package kr.co.wisenut.editor.model;

public class Event {
	private String clasCd;
	private String clasNm;
	private String upperClas;
	
	public String getClasCd() {
		return clasCd;
	}
	public void setClasCd(String clasCd) {
		this.clasCd = clasCd;
	}
	public String getClasNm() {
		return clasNm;
	}
	public void setClasNm(String clasNm) {
		this.clasNm = clasNm;
	}
	public String getUpperClas() {
		return upperClas;
	}
	public void setUpperClas(String upperClas) {
		this.upperClas = upperClas;
	}
}
