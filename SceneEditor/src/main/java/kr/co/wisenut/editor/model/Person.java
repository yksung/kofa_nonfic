package kr.co.wisenut.editor.model;

public class Person {
	private String korNm;
	private String engNm;
	private String nationalClss;
	private String personField;
	public String getKorNm() {
		return korNm;
	}
	public void setKorNm(String korNm) {
		this.korNm = korNm;
	}
	public String getEngNm() {
		return engNm;
	}
	public void setEngNm(String engNm) {
		this.engNm = engNm;
	}
	public String getNationalClss() {
		return nationalClss;
	}
	public void setNationalClss(String nationalClss) {
		this.nationalClss = nationalClss;
	}
	public String getPersonField() {
		return personField;
	}
	public void setPersonField(String personField) {
		this.personField = personField;
	}
	
	
}
