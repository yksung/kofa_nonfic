package kr.co.wisenut.editor.model;

public class ScenePersonMapping {
	private int recid;
	private String scnId;
	private String vdoId;
	private String personId;
	private String korNm;
	private String engNm;
	private String nationalClss;
	private String personField;
	
	public String getScnId() {
		return scnId;
	}
	public void setScnId(String scnId) {
		this.scnId = scnId;
	}
	public String getVdoId() {
		return vdoId;
	}
	public void setVdoId(String vdoId) {
		this.vdoId = vdoId;
	}
	public int getRecid() {
		return recid;
	}
	public void setRecid(int recid) {
		this.recid = recid;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	public String getKorNm() {
		return korNm;
	}
	public void setKorNm(String korNm) {
		this.korNm = korNm;
	}
	public String getEngNm() {
		return engNm;
	}
	public void setEngNm(String engNm) {
		this.engNm = engNm;
	}
	public String getNationalClss() {
		return nationalClss;
	}
	public void setNationalClss(String nationalClss) {
		this.nationalClss = nationalClss;
	}
	public String getPersonField() {
		return personField;
	}
	public void setPersonField(String personField) {
		this.personField = personField;
	}
	
	
}
