package kr.co.wisenut.search.model;

public class MatchedText {
	String string;
	int begin;
	int end;
	public String getString() {
		return string;
	}
	public void setString(String string) {
		this.string = string;
	}
	public int getBegin() {
		return begin;
	}
	public void setBegin(int begin) {
		this.begin = begin;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	
}
